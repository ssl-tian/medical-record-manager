    def _build_notice(self, patient_name, version, total_days,
                      phase_1_name, phase_1_days, phase_1_content,
                      phase_2_name, phase_2_days, phase_2_content,
                      faq, extra_notes):
        title = f"# {patient_name}入院须知"
        if version == "alcohol":
            title += "（酒精依赖）"
        daily_schedule = self._daily_schedule_table()
        lines = [
            title, "",
            "感谢您对本科室的信任。为了帮助您了解住院期间的治疗安排，请仔细阅读以下内容。",
            "如有疑问，可随时向主管医生或责任护士咨询。", "", "---", "",
            "## 一、治疗流程简介", "",
            f"预计总住院时长：**{total_days}**", "",
            f"### 阶段一：{phase_1_name}（预计{phase_1_days}）", "",
            phase_1_content, "",
            f"### 阶段二：{phase_2_name}（预计{phase_2_days}）", "",
            phase_2_content, "",
            "### 典型一日安排", "",
            "以下为病区常规作息时间，具体以病区实际安排为准：", "",
            daily_schedule, "", "---", "",
            "## 二、入院常见问题", "",
            faq, "", "---", "",
        ]
        if extra_notes:
            lines.extend(["## 三、温馨提示", ""])
            for note in extra_notes:
                lines.append(f"- {note}")
            lines.extend(["", ""])
        lines.extend([
            f"> 本须知由医生个人制作，如有更新以病区最新公告为准。",
            "", "---", "",
            f"*生成日期：{self._today()}*",
        ])
        return "\n".join(lines)

